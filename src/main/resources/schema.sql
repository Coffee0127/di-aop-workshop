DROP TABLE IF EXISTS EMP2;
DROP TABLE IF EXISTS DEPT2;

CREATE TABLE DEPT2 (
  DEPTNO INT GENERATED BY DEFAULT AS IDENTITY (START WITH 50, INCREMENT BY 10) NOT NULL,
  DNAME  VARCHAR(14),
  LOC    VARCHAR(13),
  CONSTRAINT DEPT2_PRIMARY_KEY PRIMARY KEY (DEPTNO)
);

CREATE TABLE EMP2 (
  EMPNO    INT GENERATED BY DEFAULT AS IDENTITY (START WITH 7015, INCREMENT BY 10) NOT NULL,
  ENAME    VARCHAR(10),
  JOB      VARCHAR(9),
  HIREDATE DATE,
  SAL      INT,
  COMM     DOUBLE,
  DEPTNO   INT NOT NULL,
  CONSTRAINT EMP2_DEPTNO_FK FOREIGN KEY (DEPTNO) REFERENCES DEPT2 (DEPTNO),
  CONSTRAINT EMP2_EMPNO_PK PRIMARY KEY (EMPNO)
);
